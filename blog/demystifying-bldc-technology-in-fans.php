<p class="blog-sup-point"><strong>What is BLDC Technology?</strong></p>
<p>BLDC stands for Brushless Direct Current and as the name implies, it operates on Direct Current electricity unlike the conventional DC fans that use Alternating Current. Just like the transition of incandescent bulbs to <a href="https://www.ivas.homes/electricals/leds/category/ceiling-led-lights/">LED bulbs</a> that took place a few years ago, conventional fans are now being replaced with BLDC fans now. The main component of BLDC fans is the motor that can bring down the power consumption of fans by up to 65% compared to the conventional induction motors.
</p>

<p>When it comes to the motor, let us dive a little into the mechanics of how it works. All the motors are made up of three essential components namely stator, rotor, and shaft. A stator is the stationary object, while rotor is the moving/ rotating object, and the shaft is what connects the motor to the application. These components are mounted in the motor based on the application of power supply. BLDC fans use DC current which is the easiest to comprehend.</p>

<p>A permanent magnet is fixed in the rotor while the stator has copper windings used in it. This leads to the magnetic drift when the electric current is passed through it. Rotational movements are exhorted by the magnetic force as the north and south polarities are placed on the rotor and stator. To impel continuous movement, polarity in the stator needs to be altered. Hence, as the same computation takes place in rotor as well as stator, continuous rotation takes place.
</p>

<p>The constant change in the stator’s polarity is driven by an electronic charge. Now in BLDC drive, the major components that are used to propel the mechanics are as follows:</p>

<ul class="disc">
<li>SMPS – converts AC supply into DC supply</li>
<li>Microcontroller – receives the input data from remote, gives inverter’s output data</li>
<li>Inverter – Drives signals received from the microcontroller to the motor</li>
</ul>

<p>The major difference between the BLDC and standard DC fan is the technique of changing the direction of the current for the rotational movement in the motor. In BLDC motor, due to the absence of brushes, commutation happens by driving algorithms in electronics as expanded above. While in the case of DC motor, there is a high chance of brushes getting worn out with time as compared to BLDC motor where this is eliminated due to the absence of brushes.
</p>

<p class="blog-sup-point"><strong>Why Choose BLDC Fans?</strong></p>
<p>Fans driven by BLDC technology consume less energy compared to conventional fans. Where the DC fans would consume somewhere around 75 watts, the BLDC fans would use 30 watts. They are also quieter than the usual fans along with being minimalist in their appearance which makes them easily adaptable to any space.</p>
<p><a href="https://www.ivas.homes/electricals/fans/category/ceiling-fans/">Ceiling fans</a> are the utility in every household as they are used throughout the day as cooling systems. This leads to massive energy consumption in residential as well as commercial spaces. With rising concerns about the environment and its protection, it has become imperative to prevent increased energy consumption and save resources available for power generation. Hence, BLDC fans will see a wide usage in the times to come given its energy-efficient nature.</p>




