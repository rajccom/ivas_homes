			<h6>You need to protect your product from:</h6>
			<ol>
        <li><b>Water scaling:</b> Due to presence of minerals and salt in water,scaling is formed on the product if it is not cleaned regular. The products should be cleaned gently at regular intervals. Also,take extra care to clean the edges and corners of the products because these areas are more vulnerable for scale settling. Clean the aerators frome time to time for a perfect foam flow fo water from the fittings. The property of water being used is very imortant for your plumbing installations (Pipes etc.),as well as for your own hygiene. The hardness of water should not exceed 300-400ppm.</li>

        <li><b>Acid fumes:</b> When we clean the tiles or floors in the bathroom with cleaning agent which contain acids or chemicals, acid fumes are formed. Because the basic material used in the products is brass, brass being an alloy of copper reacts with these acid fumes. With this reaction, a green colour layer is formed on the products resulting in damage to the outer finish or coating.</li>

        <li><b>Scratching while cleaning:</b>  scratchimg or scrubbing occurs due to hard nature of cloth or coarse cleaning powder being used for cleaning.Use a gentle fluffy soft cloth for cleaning the surface.</li>

        <li><b>Atmospheric conditions:</b>  This condition applies mostly to salt content in the air in coastal areas, sometime the air reacts with brass and black spots emerge on the surface of product. Therefore, the product is required to be cleaned regularly.At the time of installation of these products,cover the products with cloth covers provided in packaging or with polythene till the time you actually start using them.</li>
      </ol>

<h6>Care and cleaning guidelines for Sanitary Ware Products.</h6>
<ul>
<li>Viterous china sanitary ware products are fragile in nature and need to be handled with care(to avoid personal injury) at all times during its storage,handling,installatiion and use thereafter.</li>
<li>The wash basin and water closet are heavy and it recommended that two persons hold it while it is being fixed. </li>
<li>Carefully inspect the new product for damage before installation.</li>
<li>Risk of product damage.Over tightening may cause breakage or chipping of viterous china.</li>
<li>Risk of external leakage.Do not lift or rock the bowl after placement.</li>
<li>Always test product cleaning solution on an inconspicuous area on the product before applying it to the entire surface.</li>
<li>Do not allow cleaners to sit or soak on the product.</li>
<li>Use a soft, dampened sponge or cloth to clean. Never use abrasive material like a bristle brush or scouring pad.</li>
<li>Wipe surface clean and rinse completely with water immediately after applying cleansers.</li>
